package javapart1;

public class DataTypes 
{
	public static void main(String[] args) 
	{
    
	//Datatype describes the type of the value/data which we need to 
	//store in a variable.
	//These are all primitive data types
	int i=10;
	double d=20.5;
	char c='H';
	boolean b=false;
	
	//String is a predefined class and it acts as a datatype.So we have 
	//to start all the datatypes with lowerclass and string with upperclass
	//as we mention the class names with uppercase.
	String s="welcome";
	
	
	System.out.println(i);
	System.out.println(d);
	System.out.println(c);
	System.out.println(b);
	System.out.println(s);
	

	}

}
